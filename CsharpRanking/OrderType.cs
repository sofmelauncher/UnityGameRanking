namespace Ranking
{
    /// <summary>
    /// 昇順 or 降順
    /// </summary>
    public enum OrderType
    {
        /// <summary>
        /// ASC(昇順)
        /// 少ない方が上位
        /// </summary>
        ASC,
        /// <summary>
        /// DESC(降順)
        /// 大きい方が上位
        /// </summary>
        DESC
    }
}
